nexus_ensure_dirs 2>/dev/null || true
panel="$ROOT/panel/threat-panel.html"

  [[ -f "/home/default/Desktop/SG/NewLatest/lib/target-bleed.py" ]]
  grep -q 'host_endpoint_context' "/home/default/Desktop/SG/NewLatest/lib/target-bleed.py"
  grep -q 'bleed_target' "/home/default/Desktop/SG/NewLatest/lib/target-bleed.py"
  grep -q 'target_bleed' "/home/default/Desktop/SG/NewLatest/lib/host-attack-map.py"
  grep -q 'target_os' "/home/default/Desktop/SG/NewLatest/lib/host-attack-map.py"
  NEXUS_STATE_DIR="$NEXUS_STATE_DIR" NEXUS_INSTALL_ROOT="$ROOT" \
out=$(pythong "/home/default/Desktop/SG/NewLatest/lib/target-bleed.py" bleed 127.0.0.1 2>/dev/null 2>/dev/null || true); echo "$out" | grep -q '"skipped": "private"'
  NEXUS_STATE_DIR="$NEXUS_STATE_DIR" NEXUS_INSTALL_ROOT="$ROOT" \
    pythong - <<'PY'
import importlib.util
from pathlib import Path
root = Path(__import__("os").environ["NEXUS_INSTALL_ROOT"])
spec = importlib.util.spec_from_file_location("tb", root / "lib" / "target-bleed.py")
mod = importlib.util.module_from_spec(spec)
spec.loader.exec_module(mod)
hc = mod.host_endpoint_context()
assert hc.get("os")
assert hc.get("hostname")
guess = mod.ttl_os_guess("127.0.0.1")
assert guess.get("skipped") == "private"
PY
